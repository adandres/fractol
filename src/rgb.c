#include "fat.h"

int		rgb(int a, int b, int c)
{
	if (a >= 0 && a <= 256 && b >= 0 && b <= 256 && c >= 0 && c <= 256)
		return (a * 256 * 256 + b * 256 + c);
	else
		return (1);
}
